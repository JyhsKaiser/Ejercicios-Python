'''
Metodo 'title()':
Este metodo convierte la letra de cada palabra de una cadena en mayusculas
'''
cadena = 'hoa bebe'
print(cadena.title())

'''
Metodo 'istitle()':
Este metodo nos devuelve un booleano, si las letras de cada palabra estan en mayusculas nos devuelve true si no, false
'''
cadena_istitle_false = 'hola Mundo'
if cadena_istitle_false:
    print(cadena_istitle_false,'es falso')

cadena_istitle_true = 'Hola Mundo'
if cadena_istitle_true:
    print(cadena_istitle_true,'es verdadero')