'''
Metodo center():
Permite centrar un string añadiendop espacios o caracteres segun nosotros indiquemos
Argumentos:
    longitud: esta longitud contempla la logitud de la cadena y si la rebasa agrega mas espacios hasta llega a 10
'''
print('MENU'.center(50,'_'))
'''
Metodo ljust():
Nos permite alinear un string a la izquierda añadiendo caracteres o espacios al final de nuestro string
'''
print('MENU LEFT'.ljust(50,'_'))

'''
Metodo rjust():
Nos permite alinear un string a la derecha añadiendo caracteres o espacios al inicio de nuestro string
'''
print('MENU LEFT'.rjust(50,'_'))